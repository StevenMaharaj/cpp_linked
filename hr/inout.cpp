#include <iostream>
#include <cstdio>
using namespace std;

int main()
{
    int t;
    long r;
    char a;
    float f;
    double d;
    scanf("%d %ld %c %f %lf", &t, &r, &a, &f, &d);
    printf("%d\n", t);
    printf("%ld\n", r);
    printf("%c\n", a);
    printf("%f\n", f);
    printf("%lf\n", d);
    return 0;
}